package com.zx.promotion.datamodel;

import java.io.Serializable;

public class HostStock implements Serializable {
    private boolean sellBuyFlag;
    private int transactionType;
    private String title;
    private String name;
    private String picture;
    private String dateAvailable;
    private int quantity;
    private double productPrice;
    private String remark;
    private long productId;

    public boolean isSellBuyFlag() {
        return sellBuyFlag;
    }

    public void setSellBuyFlag(boolean sellBuyFlag) {
        this.sellBuyFlag = sellBuyFlag;
    }

    public int getTransactionType() {
        return transactionType;
    }

    public void setTransactionType(int transactionType) {
        this.transactionType = transactionType;
    }

    public String getTitle() {
        return title;
    }

    public void setTitle(String title) {
        this.title = title;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getPicture() {
        return picture;
    }

    public void setPicture(String picture) {
        this.picture = picture;
    }

    public String getDateAvailable() {
        return dateAvailable;
    }

    public void setDateAvailable(String dateAvailable) {
        this.dateAvailable = dateAvailable;
    }

    public int getQuantity() {
        return quantity;
    }

    public void setQuantity(int quantity) {
        this.quantity = quantity;
    }

    public double getProductPrice() {
        return productPrice;
    }

    public void setProductPrice(double productPrice) {
        this.productPrice = productPrice;
    }

    public String getRemark() {
        return remark;
    }

    public void setRemark(String remark) {
        this.remark = remark;
    }

    public long getProductId() {
        return productId;
    }

    public void setProductId(long productId) {
        this.productId = productId;
    }
}
