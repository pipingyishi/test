package com.zx.crawler.stock.util;

public class PropertieUtil {
    private String hostUser;
    private String referenceInformation;
    private String hostConnect;
    private String telphone;
    private String bankType;

    public String getHostUser() {
        return hostUser;
    }

    public void setHostUser(String hostUser) {
        this.hostUser = hostUser;
    }

    public String getReferenceInformation() {
        return referenceInformation;
    }

    public void setReferenceInformation(String referenceInformation) {
        this.referenceInformation = referenceInformation;
    }

    public String getHostConnect() {
        return hostConnect;
    }

    public void setHostConnect(String hostConnect) {
        this.hostConnect = hostConnect;
    }

    public String getTelphone() {
        return telphone;
    }

    public void setTelphone(String telphone) {
        this.telphone = telphone;
    }

    public String getBankType() {
        return bankType;
    }

    public void setBankType(String bankType) {
        this.bankType = bankType;
    }

}
